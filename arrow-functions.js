/* Задача 6. Напиши функцию, которая принимает два параметра - строки и возвращает ту, что длинее. */

let llength = (h, i) => {
    if (h.length > i.length) {
        return h
    } else {
        return i
    }
}
console.log(llength('Often', 'Not so often'));

