/* Задача 4. Напиши функцию, которая прининимает два параметра - числа и возвращает наибольшее из них. */

let bolsh = (e, f) => {
    if (e > f) {
        return e
    }
    else {
        return f
    }
}
console.log(bolsh(10, 5));

