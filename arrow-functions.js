/* Задача 1. Напиши функцию, которая принимает 1 параметр - число, и возвращает строку четное оно или нечетное. */

let num = (a) => {
    return a % 2 === 0 ? 'chetnoe' : 'nechetnoe'
}
console.log(num(5));
console.log(num(6));

